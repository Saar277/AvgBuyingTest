package org.example.finals;

public class Finals {
    public static int START_YEAR;
    public static int START_MONTH;
    public static int END_YEAR;
    public static int END_MONTH;

    public static double LAST_PRICE;

    public static void setStartYear(int startYear) {
        START_YEAR = startYear;
    }

    public static void setStartMonth(int startMonth) {
        START_MONTH = startMonth;
    }

    public static void setEndYear(int endYear) {
        END_YEAR = endYear;
    }

    public static void setEndMonth(int endMonth) {
        END_MONTH = endMonth;
    }

    public static void setLastPrice(double lastPrice) {
        LAST_PRICE = lastPrice;
    }
}
